AsciiAtlas::AsciiAtlas() = default;

AsciiAtlas::~AsciiAtlas()
{
  destroy();
}

AsciiAtlas::AsciiAtlas(AsciiAtlas&& o) noexcept
  : m_texId(o.m_texId)
  , m_glyphCount(o.m_glyphCount)
  , m_glyphPx(o.m_glyphPx)
{
  o.m_texId = 0;
}

AsciiAtlas& AsciiAtlas::operator=(AsciiAtlas&& o) noexcept
{
  if (this != &o) {
    destroy();
    m_texId      = o.m_texId;
    m_glyphCount = o.m_glyphCount;
    m_glyphPx    = o.m_glyphPx;
    o.m_texId    = 0;
  }
  return *this;
}

void AsciiAtlas::destroy()
{
  if (m_texId) {
    glDeleteTextures(1, &m_texId);
    m_texId = 0;
  }
}

bool AsciiAtlas::build(const unsigned char* /*ttfData*/, int /*ttfBytes*/,
                       const std::string& charset, glm::ivec2 glyphPx)
{
  if (charset.empty()) {
    spdlog::error("AsciiAtlas::build: empty charset");
    return false;
  }

  const int Gw = glyphPx.x;
  const int Gh = glyphPx.y;

  if (Gw <= 0 || Gh <= 0) {
    spdlog::error("AsciiAtlas::build: invalid glyph size {}x{}", Gw, Gh);
    return false;
  }

  const int N = static_cast<int>(charset.size());

  struct GlyphBitmap {
    std::vector<unsigned char> pixels; // Gw*Gh, 8-bit coverage (0 or 255)
    int fillCount = 0;
    char ch = 0;
  };

  std::vector<GlyphBitmap> glyphs(N);
  for (int i = 0; i < N; ++i) {
    const unsigned char code = static_cast<unsigned char>(charset[i]);
    glyphs[i].ch = charset[i];
    glyphs[i].pixels.assign(Gw * Gh, 0);

    if (code >= 128) {
      // Out of range for the built-in font — leave blank
      continue;
    }

    const uint8_t* rows = sk_font8x8[code];

    // Scale 8x8 source to Gw x Gh using nearest-neighbour
    for (int dy = 0; dy < Gh; ++dy) {
      const int sy = dy * 8 / Gh; // source row [0,7]
      for (int dx = 0; dx < Gw; ++dx) {
        const int sx = dx * 8 / Gw; // source col [0,7]
        const bool lit = (rows[sy] >> sx) & 1;
        if (lit) {
          glyphs[i].pixels[dy * Gw + dx] = 255;
          ++glyphs[i].fillCount;
        }
      }
    }
  }

  // Sort darkest (fewest lit pixels) to brightest
  std::sort(glyphs.begin(), glyphs.end(),
            [](const GlyphBitmap& a, const GlyphBitmap& b) {
              return a.fillCount < b.fillCount;
            });

  // Pack into a single-row atlas: width = N*Gw, height = Gh
  std::vector<unsigned char> atlasPixels(N * Gw * Gh, 0);
  for (int i = 0; i < N; ++i) {
    for (int row = 0; row < Gh; ++row) {
      const int srcOff = row * Gw;
      const int dstOff = row * (N * Gw) + i * Gw;
      std::memcpy(atlasPixels.data() + dstOff, glyphs[i].pixels.data() + srcOff, Gw);
    }
  }

  // Upload to GL
  destroy();
  glGenTextures(1, &m_texId);
  glBindTexture(GL_TEXTURE_2D, m_texId);
  glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
  glTexImage2D(GL_TEXTURE_2D, 0, GL_R8,
               N * Gw, Gh, 0,
               GL_RED, GL_UNSIGNED_BYTE, atlasPixels.data());
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
  glBindTexture(GL_TEXTURE_2D, 0);

  m_glyphCount = N;
  m_glyphPx    = glyphPx;

  spdlog::debug("AsciiAtlas: built {} glyphs, atlas {}x{}", N, N * Gw, Gh);
  return true;
}
